package cn.orz.pascal.example.saprk;

import java.io.Serializable;

/**
 *
 * @author koduki
 */
public class Item implements Serializable {

    long id;
    String value;

    public Item(long id, String value) {
        this.id = id;
        this.value = value;
    }

    public long getId() {
        return id;
    }

    public String getValue() {
        return value;
    }

}
